int	compare_grid(int grid[9][9], int grid2[9][9])
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (y < 9)
	{
		if (grid[y][x] != grid2[y][x])
			return (0);
		x++;
		if (x == 9)
		{
			x = 0;
			y++;
		}
	}
	return (1);
}
