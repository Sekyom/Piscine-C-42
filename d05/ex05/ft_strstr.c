char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int k;
	int j;

	i = 0;
	k = 0;
	while (str[i] != 0)
	{
		j = k;
		while (str[i] == to_find[k])
		{
			i++;
			k++;
			if (to_find[k] == '\0')
			{
				j = k;
				k = 0;
				return (&str[i - j]);
			}
			i -= j;
		}
		i++;
	}
	return (0);
}
