int	ft_is_prime(int nb)
{
	unsigned long	dif;

	dif = 2;
	if (nb < 1)
	{
		return (0);
	}
	while (nb >= dif * dif)
	{
		if (nb % dif == 0)
			return (0);
		dif++;
	}
	return (1);
}
